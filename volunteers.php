<p>The Cast Assistants will serve as volunteers from his/her stake and will work under the direction of the 
  team leaders who will report to the Director. The Cast Assistant Volunteer will also work in conjunction 
  with the Stage Crew and Young Single Adult Volunteers. All Cast Assistant Volunteers will need to be at 
  BOTH performances and ALL rehearsals.</p>
<p>The Cast Assistant Volunteers will be responsible for the following jobs:
  <ul>
    <li>Gather all the permission slips from the youth and keep them on file until the end of October.</li>
    <li>Arrange travel for all the youth from your stake. We need to know how many drivers there will be. 
      Please arrange for carpools with at least four or five youth per car. Make sure everyone has a seat belt. 
      Print parking passes and hand them out to the drivers.</li>
    <li>Provide non-perishable lunches for the youth in your stake. More specific information will be provided 
      at our meeting on the 23rd of August. You also need to provide 2 five-gallon jugs of ice water and cups 
      that we will use at our water station. Make sure water jugs are marked.</li>
    <li>Assist the cast, during rehearsals and performances, to move quickly and quietly where they need to be.</li>
    <li>On the day of the performance, be responsible for checking in the cast members from your stake and 
      keeping track of them during the day.</li>
    <li>Remain after performances to help clean up the area and take down the set.</li>
  </ul>
</p>
<p>Calendar Dates:
  <ul>
    <li>August 23rd - 10:00 am to 12:00 pm @7989 South 2250 East, South Weber, UT - Training Meeting</li>
    <li>September 19th - Time to be determined - As many as are available to help with set up @ Dee Events 
      Center</li>
    <li>September 19th - 6:00 pm - rehearsal @ Dee Events Center</li>
    <li>September 20th - ALL DAY starting at 7:30 am - rehearsals and performances @ Dee Events Center</li>
  </ul>
</p>
<p>Items to Bring:
  <ul>
    <li>Watch (or something to keep track of time)</li>
    <li>Lunch &amp; dinner on the 20th</li>
    <li>WATER!!!!</li>
    <li>Wear black shirt &amp; black pants or skirt and comfortable shoes - preferably black. We will be 
      outside most of the day so please a hat and a lawn chair.</li>
  </ul>
</p>
<p>Thank you so much for your willingness to help with this once in a lifetime opportunity for the youth in 
  our temple district! We look forward to meeting all of you at our first meeting!<br/><br/>
